namespace TextPresenter51456 {
    public struct ActualScreenRect {
        public double x;
        public double y;
        public double width;
        public double height;

        public ActualScreenRect(double x, double y, double width, double height) {
            this.x = x;
            this.y = y;
            this.width = width;
            this.height = height;
        }
    }
}